#include <stdint.h>
#include <stdio.h>

void encrypt (uint32_t v[2], const uint32_t k[4]) {
    uint32_t v0=v[0], v1=v[1], sum=0, i;           /* set up */
    uint32_t delta=0x9E3779B9;                     /* a key schedule constant */
    uint32_t k0=k[0], k1=k[1], k2=k[2], k3=k[3];   /* cache key */
    for (i=0; i<32; i++) {                         /* basic cycle start */
        sum += delta;
        v0 += ((v1<<4) + k0) ^ (v1 + sum) ^ ((v1>>5) + k1);
        v1 += ((v0<<4) + k2) ^ (v0 + sum) ^ ((v0>>5) + k3);
    }                                              /* end cycle */
    v[0]=v0; v[1]=v1;
}

void decrypt (uint32_t v[2], const uint32_t k[4]) {
    uint32_t v0=v[0], v1=v[1], sum=0xC6EF3720, i;  /* set up; sum is 32*delta */
    uint32_t delta=0x9E3779B9;                     /* a key schedule constant */
    uint32_t k0=k[0], k1=k[1], k2=k[2], k3=k[3];   /* cache key */
    for (i=0; i<32; i++) {                         /* basic cycle start */
        v1 -= ((v0<<4) + k2) ^ (v0 + sum) ^ ((v0>>5) + k3);
        v0 -= ((v1<<4) + k0) ^ (v1 + sum) ^ ((v1>>5) + k1);
        sum -= delta;
    }                                              /* end cycle */
    v[0]=v0; v[1]=v1;
}

const uint32_t KEY[] = {0x46616e63, 0x79414375, 0x704f6654, 0x45413f3f};

int main(void) {
    uint32_t data[]=   {0x4354467b, 0x57686f2d, 0x6e656564, 0x732d616e,
                        0x2d465047, 0x412d616e, 0x79776179, 0x2d2d2d7d};
    encrypt(data, KEY);
    encrypt(data+2, KEY);
    encrypt(data+4, KEY);
    encrypt(data+6, KEY);
    printf("%x%x%x%x%x%x%x%x\n", data[0], data[1], data[2], data[3], data[4], data[5], data[6], data[7]);
    return 0;
}
